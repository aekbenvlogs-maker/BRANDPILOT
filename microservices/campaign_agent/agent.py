_REDIS_CHANNEL_PREFIX = "campaign:event"


async def _publish_event(event: str, payload: dict[str, Any]) -> None:
    """Publish a campaign lifecycle event to Redis pub/sub.

    Frontend subscribers (WebSocket gateway) listen on these channels to
    push real-time updates without polling.

    Args:
        event:   Event name, e.g. ``"campaign.ready"``.
        payload: Arbitrary JSON-serialisable dict.
    """
    settings = get_settings()
    try:
        async with aioredis.from_url(settings.redis_url) as redis:
            channel = f"{_REDIS_CHANNEL_PREFIX}:{event}"
            await redis.publish(channel, json.dumps(payload))
            logger.debug(
                "[CampaignAgent] Event published | channel={} payload={}",
                channel,
                payload,
            )
    except Exception as exc:  # pragma: no cover
        # Notification failure must never abort the main pipeline.
        logger.warning(
            "[CampaignAgent] Redis publish failed (non-fatal) | event={} error={}",
            event,
            exc,
        )


