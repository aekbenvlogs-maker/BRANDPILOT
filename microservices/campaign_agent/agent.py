@dataclass
class SocialPost:
    """A single scheduled social media post generated by the Campaign Agent.

    Attributes:
        id:               UUID of this post.
        campaign_id:      Parent campaign UUID.
        platform:         Target social platform (instagram, tiktok, …).
        content_text:     Generated caption / body text.
        media_urls:       List of S3 media asset URLs.
        hashtags:         Suggested hashtags.
        scheduled_at:     UTC datetime when the post should go live.
        status:           Lifecycle state of the post.
        platform_post_id: ID returned by the social API after publishing.
        published_at:     UTC datetime when the post was actually published.
        celery_task_id:   Celery ETA task ID for revocation on cancel.
    """

    id: str = field(default_factory=lambda: str(uuid.uuid4()))
    campaign_id: str = ""
    platform: str = ""
    content_text: str = ""
    media_urls: list[str] = field(default_factory=list)
    hashtags: list[str] = field(default_factory=list)
    scheduled_at: str = ""  # ISO-8601 UTC string
    status: str = "draft"  # draft|pending_validation|approved|scheduled|published|failed|cancelled
    platform_post_id: str | None = None
    published_at: str | None = None
    celery_task_id: str | None = None


@dataclass
class CampaignValidationRecord:
    """Audit trail entry for a campaign approval / rejection action.

    Attributes:
        id:             UUID of this record.
        campaign_id:    Campaign being validated.
        user_id:        User who performed the action.
        action:         "approved" | "rejected" | "modified".
        modified_posts: JSONB snapshot of any modified posts.
        validated_at:   UTC ISO-8601 timestamp.
    """

    id: str = field(default_factory=lambda: str(uuid.uuid4()))
    campaign_id: str = ""
    user_id: str = ""
    action: str = "approved"
    modified_posts: list[dict[str, Any]] = field(default_factory=list)
    validated_at: str = field(
        default_factory=lambda: datetime.now(UTC).isoformat()
    )


@dataclass
class BrandContext:
    """Brand and account context loaded from the database.

    Attributes:
        project_id:       UUID of the project.
        brand_name:       Display name of the brand.
        brand_tone:       Detected brand tone (energetic / professional / …).
        social_accounts:  List of connected platform account IDs.
        target_audience:  Free-form audience description from brand analysis.
    """

    project_id: str
    brand_name: str = ""
    brand_tone: str = "professional"
    social_accounts: list[str] = field(default_factory=list)
    target_audience: str = ""


@dataclass
class ExecutionPlan:
    """Microservice routing plan produced by the execution planner.

    Attributes:
        microservices: Ordered list of microservice names to invoke.
        parallel_tasks: Names of tasks that can run concurrently in the chord.
        estimated_duration_s: Rough wall-clock estimate in seconds.
    """

    microservices: list[str]
    parallel_tasks: list[str]
    estimated_duration_s: int = 30


