async def _load_campaign_with_owner(
    db: AsyncSession,
    campaign_id: str,
) -> tuple[Campaign, str]:
    """Load a Campaign and resolve its owner user_id via Project join.

    Args:
        db:          Async database session.
        campaign_id: String UUID of the campaign.

    Returns:
        Tuple of (Campaign ORM instance, owner user_id string).

    Raises:
        CampaignNotFoundError: Campaign does not exist.
    """
    stmt = (
        select(Campaign, Project.user_id)
        .join(Project, Campaign.project_id == Project.id)
        .where(Campaign.id == uuid.UUID(campaign_id))
    )
    result = await db.execute(stmt)
    row = result.first()
    if row is None:
        raise CampaignNotFoundError(campaign_id)
    campaign, owner_id = row
    return campaign, str(owner_id)


async def _load_agent_job(
    db: AsyncSession,
    campaign_id: str,
) -> WorkflowJob | None:
    """Return the WorkflowJob associated with a campaign agent run.

    Args:
        db:          Async database session.
        campaign_id: String UUID of the campaign.

    Returns:
        Most-recent matching WorkflowJob or None.
    """
    result = await db.execute(
        select(WorkflowJob)
        .where(WorkflowJob.campaign_id == uuid.UUID(campaign_id))
        .where(WorkflowJob.job_type == "campaign_agent")
        .order_by(WorkflowJob.created_at.desc())
        .limit(1)
    )
    return result.scalar_one_or_none()


def _posts_from_job(job: WorkflowJob) -> list[SocialPost]:
    """Deserialise SocialPost list from a WorkflowJob result payload.

    Args:
        job: WorkflowJob whose ``result`` contains serialised posts.

    Returns:
        List of SocialPost instances (empty if none stored yet).
    """
    if not job.result:
        return []
    raw_posts: list[dict[str, Any]] = job.result.get("posts", [])
    return [SocialPost(**p) for p in raw_posts]


