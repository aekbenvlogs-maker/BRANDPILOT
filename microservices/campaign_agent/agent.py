def _campaign_name(intent: CampaignIntent) -> str:
    """Build a human-readable default campaign name from the intent.

    Args:
        intent: Parsed CampaignIntent.

    Returns:
        A short descriptive name string.

    Example:
        >>> _campaign_name(CampaignIntent(platform="instagram", objective="awareness"))
        'instagram — awareness'
    """
    parts: list[str] = [intent.platform, intent.objective]
    if intent.product_description:
        # Keep it concise: first 30 chars of the product description.
        parts.append(intent.product_description[:30])
    return " — ".join(parts)
