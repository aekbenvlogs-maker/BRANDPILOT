class CampaignNotFoundError(BrandpilotError):
    """Raised when a campaign_id does not exist in the database."""

    def __init__(self, campaign_id: str) -> None:
        super().__init__(
            message=f"Campaign '{campaign_id}' not found.",
            code="CAMPAIGN_NOT_FOUND",
        )


class InvalidStatusError(BrandpilotError):
    """Raised when an operation is invalid for the campaign's current status.

    Attributes:
        current_status: The actual status found in the DB.
        expected_status: The status required by the operation.
    """

    def __init__(self, current_status: str, expected_status: str) -> None:
        super().__init__(
            message=(
                f"Cannot perform operation: campaign is '{current_status}', "
                f"expected '{expected_status}'."
            ),
            code="INVALID_STATUS",
        )
        self.current_status = current_status
        self.expected_status = expected_status


class UnauthorizedError(BrandpilotError):
    """Raised when the requesting user does not own the campaign."""

    def __init__(self, campaign_id: str, user_id: str) -> None:
        super().__init__(
            message=(
                f"User '{user_id}' is not authorized to modify campaign "
                f"'{campaign_id}'."
            ),
            code="UNAUTHORIZED",
        )


